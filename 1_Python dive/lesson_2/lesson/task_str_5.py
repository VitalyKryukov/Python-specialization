"""
Напишите небольшую программу, которая
запрашивает у пользователя текст.
Если текст можно привести к целому числу,
выведите его двоичное, восьмиричное
и шестнадцатиричное представление.
А если преобразование к целому невозможно,
сообщите написан ли текст в ASCII или нет.
"""


def check_and_convert(text: str) -> None:
    try:
        # Пробуем привести текст к целому числу
        number = int(text)

        # Если успешно, выводим его различные представления
        print(f"Двоичное представление: {bin(number)}")
        print(f"Восьмеричное представление: {oct(number)}")
        print(f"Шестнадцатеричное представление: {hex(number)}")
    except ValueError:
        # Если преобразование не удалось, проверяем, написан ли текст в ASCII и является ли печатаемым
        if text.isascii() and text.isprintable():
            print("Текст написан в ASCII и состоит из печатаемых символов.")
        else:
            print("Невозможно преобразовать текст к целому числу или текст не соответствует требованиям.")


# Запрашиваем у пользователя ввод текста
user_input = input("Введите текст: ")

# Вызываем функцию для обработки введенного текста
check_and_convert(user_input)

import doctest
from string import ascii_lowercase

ascii_lowercase += ' '


def symbol_deleter(text):
    '''
    >>> symbol_deleter('Something string') == 'something string'
    True
    >>> symbol_deleter('SOMETHING STRING') == 'something string'
    True
    >>> symbol_deleter('something! string?') == 'something string'
    True
    >>> symbol_deleter('something—Å–∞–º—Ñ–∏–Ω–∫ string—Å—Ç—Ä–∏–Ω–≥') == 'something string'
    True
    >>> symbol_deleter('SOMETHING–°–ê–ú–§–ò–ù–ì! string?') == 'something string'
    True
    '''

    if not isinstance(text, str):
        raise ValueError
    return ''.join([i.lower() for i in text if i.lower() in ascii_lowercase])


if __name__ == '__main__':
    doctest.testmod(verbose=True)
